    {
        Guest::create([
            'name'       => $request->name,
            'phone'      => $request->phone,
            'email'      => $request->email,
            'status'     => 1,
            'group_id'   => $request->group,
            's_group_id' => $request->sgroup,
        ]);
        return response()->json(["response" =>true]);
    }

